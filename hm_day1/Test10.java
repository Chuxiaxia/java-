package hm_day1;

public class Test10 {

	public static void main(String[] args) {

		int x = 100;
		int y = 200;
		// 定义变量，求和打印到控制台
		int add = x + y;
		System.out.println("x,y的和为:" + add);

		// 定义变量，求差打印到控制台
		int sub = x - y;
		System.out.println("x,y的差为:" + sub);

		// 定义变量，求积打印到控制台
		int mul = x * y;
		System.out.println("x,y的积为:" + mul);

		// 定义变量，求商打印到控制台 . 两个整型数“/” 表示除法取整
		int div = x / y;
		System.out.println("x,y的商为:" + div);
	}

}
